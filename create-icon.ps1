# Create a minimal PNG icon (1x1 pixel transparent)
# PNG header for 1x1 transparent image
$pngBytes = [byte[]] @(
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
    0x00, 0x00, 0x00, 0x0D,                            # IHDR chunk size
    0x49, 0x48, 0x44, 0x52,                            # IHDR
    0x00, 0x00, 0x00, 0x01,                            # width: 1
    0x00, 0x00, 0x00, 0x01,                            # height: 1
    0x08, 0x06,                                        # bit depth: 8, color type: 6 (RGBA)
    0x00, 0x00, 0x00,                                  # compression, filter, interlace
    0x1F, 0x15, 0xC4, 0x89,                            # CRC
    0x00, 0x00, 0x00, 0x0A,                            # IDAT chunk size
    0x49, 0x44, 0x41, 0x54,                            # IDAT
    0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00, 0x05,   # compressed data
    0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4,               # CRC
    0x00, 0x00, 0x00, 0x00,                            # IEND chunk size
    0x49, 0x45, 0x4E, 0x44,                            # IEND
    0xAE, 0x42, 0x60, 0x82                             # CRC
)

[IO.File]::WriteAllBytes("$PSScriptRoot/assets/adaptive-icon.png", $pngBytes)
Write-Host "Created adaptive-icon.png"
